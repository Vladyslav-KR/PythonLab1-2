# Завдання 1: введення 4-х чисел
a = int(input("Введіть перше ціле число: "))
b = float(input("Введіть друге дробове число: "))
c = int(input("Введіть третє ціле число: "))
d = float(input("Введіть четверте дробове число: "))

# Завдання 2: математичні операції
div_result = d / b if b != 0 else "Ділення на нуль"
int_div_result = c // a if a != 0 else "Цілочисленне ділення на нуль"
mod_result = int(d) % b if b != 0 else "Остача від ділення на нуль"

results = [
    a + b,         # додавання
    c - d,         # віднімання
    a * c,         # множення
    div_result,    # ділення з перевіркою
    a ** 2,        # піднесення до степеня
    int_div_result,# цілочисленне ділення з перевіркою
    mod_result     # остача від ділення з перевіркою
]

# Завдання 3: кількість елементів і парні елементи
print("\nКількість елементів у списку:", len(results))

print("Парні елементи списку:")
for value in results:
    if isinstance(value, int) and value % 2 == 0:
        print(value)

# Завдання 4: обмін 2-го і 5-го елементів
results[1], results[4] = results[4], results[1]
print("\nСписок після обміну 2-го і 5-го елементів:")
print(results)

